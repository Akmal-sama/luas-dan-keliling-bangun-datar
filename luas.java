public class luas{
	static void l_persegi(double s){
		double hasil = s * s;
		System.out.println("Hasil : " + hasil);
	}

	static void l_persegi_panjang(double p , double l){
		double hasil = p * l;
		System.out.println("Hasil : " + hasil);
	}

	static void l_segitiga(double a , double t){
		double hasil = a * t / 2;
		System.out.println("Hasil : " + hasil);
	}
}