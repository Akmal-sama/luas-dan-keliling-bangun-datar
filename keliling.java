public class keliling{
	static void k_persegi(double s){
		double hasil = s * s;
		System.out.println("Hasil : " + hasil);
	}

	static void k_persegi_panjang(double p , double l){
		double hasil = p * l;
		System.out.println("Hasil : " + hasil);
	}

	static void k_segitiga(double a , double b , double c){
		double hasil = a + b + c;
		System.out.println("Hasil : " + hasil);
	}
}